#include <unistd.h>

void	ft_putaddr(void *ptr)
{
	write(1, *ptr, 1);
}

int	main(void)
{
	int	a;
	int	*ptr;

	a = 3;
	ptr = &a;
	ft_putaddr(ptr);
	return (0);
}
